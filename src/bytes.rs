//! Parsers and utilities for working with byte streams.
//!
//! The parsers in this module focuses primarily on parsing of
//! non-human readable byte sequences, but does provide some
//! utilities for interpreting bytes as various text formats.
//! When working primarily with text, the
//! [`unicode`](crate::unicode) or [`ascii`](crate::ascii)
//! modules should generally be preferred. It is also possible
//! to mix the use of these modules when needed.

use crate::{Error as PError, Failure, Input, IntoInput, Parse, ParseExt, Success, basic::pop};
use core::marker::PhantomData;

/// Generates a parser that matches a regular expression over [`ByteInput`].
///
/// [`regex!`] compiles a regular expression at compile time into a finite state
/// machine implemented as a parser over any [`ByteInput`]. The generated
/// parser succeeds if the start of the provided input matches the regex. The parsed
/// value of the parser is just the empty tuple, `()`. To capture the input that
/// matched the regex, use the [`regcognize`](ParseExt::recognize) combinator, e.g.
/// `regex!("[a-z]+").recognize()`.
///
/// Behind the scenes, this macro uses the
/// [`regex`](https://docs.rs/regex/latest/regex) crate to compile regular
/// expressions into state machines. See that crate's documentation for syntax
/// and limitations. Notably some regular expression features such as lookarounds
/// and backreferences are not supported. Capture groups are accepted, but have
/// no effect on the behavior of the generated parser other than for grouping
/// sub-expressions. The parser will always match on the entire regex.
///
/// The parser generated by this macro has full Unicode support and assumes
/// non-ASCII text is UTF-8 encoded. It also supports non-Unicode byte patterns
/// (unlike the `regex!` macros provided by the [`unicode`](crate::unicode)
/// and [`ascii`](crate::ascii) modules).
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::bytes::{PResult, regex};
/// fn my_parser(input: &[u8]) -> PResult<&[u8], &[u8]> {
///     regex!(r"[\x00-\x0f]+")
///         .recognize()
///         .ok_into()
///         .parse(input)
/// }
///
/// assert!(my_parser.parse(b"\x04\x05\x02\x16") == Ok(Success(b"\x04\x05\x02", b"\x16")));
/// assert!(my_parser.parse(b"\xff\xff\xff\xff").is_err());
/// ```
pub use pars_macros::regex_bytes as regex;

/// A byte stream parsing error.
///
/// See also [`ErrorKind`].
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Error<I: Input> {
    kind: ErrorKind,
    pos: I,
}

/// An [`ErrorSeed`](crate::ErrorSeed) for errors raised parsing a byte stream.
#[non_exhaustive]
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum ErrorKind {
    /// End of input was reached when more bytes were expected
    NeedMoreInput,

    /// More bytes were available in the input when none was expected
    ExpectedEof,

    /// Failed to parse due to invalid bytes
    InvalidInput,
}

/// Parsing result returned by byte stream parsers
pub type PResult<T, I> = super::PResult<T, I, Error<I>>;

/// Trait for parsing input symbols that each represent a byte
pub trait ByteSymbol {
    /// Extract one byte from the input stream.
    ///
    /// Unlike [`Input::next`], this function is fallible. An
    /// error may be returned when a symbol cannot be represented
    /// as a byte.
    fn parse_byte<I>(input: I) -> PResult<core::primitive::u8, I>
    where
        I: Input<Symbol = Self>;
}

/// An [`Input`] stream of bytes
pub trait ByteInput: Input {
    /// Extract one byte from the input stream.
    ///
    /// Unlike [`Input::next`], this function is fallible. An
    /// error may be returned when a symbol cannot be represented
    /// as a byte.
    fn parse_byte(self) -> PResult<core::primitive::u8, Self>;
}

impl<I: Input> Error<I> {
    /// Construct a new [`Error`] from an [`ErrorKind`] and an [`Input`] position.
    pub const fn new(kind: ErrorKind, pos: I) -> Self {
        Self { kind, pos }
    }

    /// Gets the underlying [`ErrorKind`].
    pub const fn kind(&self) -> ErrorKind {
        self.kind
    }
}

impl<I: Input> crate::Error<I> for Error<I> {
    fn need_more_input(pos: I) -> Self {
        Self::new(ErrorKind::NeedMoreInput, pos)
    }

    fn expected_eof(pos: I) -> Self {
        Self::new(ErrorKind::ExpectedEof, pos)
    }

    fn invalid_input(pos: I) -> Self {
        Self::new(ErrorKind::InvalidInput, pos)
    }

    fn position(&self) -> &I {
        &self.pos
    }
}

impl<I: Input> crate::ErrorSeed<I, Error<I>> for ErrorKind {
    fn into_error(self, pos: I) -> Error<I> {
        Error::new(self, pos)
    }
}

impl<I> ByteInput for I
where
    I: Input,
    I::Symbol: ByteSymbol,
{
    fn parse_byte(self) -> PResult<core::primitive::u8, Self> {
        <I::Symbol as ByteSymbol>::parse_byte(self)
    }
}

impl ByteSymbol for core::primitive::u8 {
    fn parse_byte<I>(input: I) -> PResult<core::primitive::u8, I>
    where
        I: Input<Symbol = Self>,
    {
        pop(input)
    }
}

impl ByteSymbol for core::primitive::i8 {
    fn parse_byte<I>(input: I) -> PResult<core::primitive::u8, I>
    where
        I: Input<Symbol = Self>,
    {
        pop.map(|b: Self| b.cast_unsigned()).parse(input)
    }
}

#[cfg(feature = "ascii")]
impl ByteSymbol for crate::ascii::AsciiChar {
    fn parse_byte<I>(input: I) -> PResult<core::primitive::u8, I>
    where
        I: Input<Symbol = Self>,
    {
        pop.map(|ch: Self| ch.as_byte()).parse(input)
    }
}

#[derive(Debug, Clone)]
struct VerbatimParser<P, I>(P, PhantomData<fn() -> I>)
where
    P: AsRef<[core::primitive::u8]>,
    I: ByteInput;

impl<P, I> Parse<I> for VerbatimParser<P, I>
where
    P: AsRef<[core::primitive::u8]>,
    I: ByteInput,
{
    type Parsed = super::Span<I>;
    type Error = Error<I>;

    fn parse<N>(&self, input: N) -> PResult<Self::Parsed, I>
    where
        N: IntoInput<Input = I>,
    {
        let input = input.into_input();
        let mut expected = self.0.as_ref();
        let mut rem = input.clone();

        while let Some(ex) = expected.next() {
            let tmp = rem.clone();
            match rem.parse_byte() {
                Ok(Success(b, new_rem)) => {
                    if b != ex {
                        return Err(Failure(PError::invalid_input(tmp), input));
                    }
                    rem = new_rem;
                }
                Err(Failure(e, _)) => {
                    return Err(Failure(e, input));
                }
            }
        }

        Ok(Success(super::Span::new(input, rem.clone()), rem))
    }
}

/// Creates a parser that exactly matches a sequence of bytes.
///
/// The returned parser succeeds if the input starts with exactly
/// the byte sequence represented by `pattern`. On success, the
/// input span that matches `pattern` is returned as the parsed
/// value.
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::bytes::{PResult, verbatim};
/// fn hello(input: &[u8]) -> PResult<&[u8], &[u8]> {
///     verbatim("hello").ok_into().parse(input)
/// }
///
/// assert!(hello.parse(b"hello world") == Ok(Success(b"hello", b" world")));
/// ```
#[inline]
pub const fn verbatim<P, I>(pattern: P) -> impl Parse<I, Parsed = super::Span<I>, Error = Error<I>>
where
    P: AsRef<[core::primitive::u8]>,
    I: ByteInput,
{
    VerbatimParser(pattern, PhantomData)
}

/// Parser that exatracts one ASCII character from the input.
///
/// If the first byte of input is not a valid ASCII character (bytes `0x00` to `0x7f`),
/// this parser will return an error. Otherwise, the byte is returned as an
/// [`AsciiChar`](crate::ascii::AsciiChar).
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::ascii::AsciiChar;
/// # use pars::bytes::{PResult, ascii_char};
/// assert!(ascii_char.parse(b"hello") == Ok(Success(AsciiChar::h, b"ello")));
///
/// assert!(ascii_char.parse(b"\xffhello").is_err());
/// ```
#[cfg(feature = "ascii")]
pub fn ascii_char<I: ByteInput>(input: I) -> PResult<crate::ascii::AsciiChar, I> {
    u8.try_map(|b| crate::ascii::AsciiChar::from_ascii(b).map_err(|_| ErrorKind::InvalidInput))
        .parse(input)
}

/// Parser that decodes one UTF-8 codepoint from the input.
///
/// This parser takes the first 1 to 4 bytes from the input depending on size of
/// the encoded codepoint, and returns the decoded [`char`]. If the input does not
/// start with a valid UTF-8 codepoint, an error is returned.
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::bytes::{PResult, utf8_char};
/// assert!(utf8_char.parse(b"hello") == Ok(Success('h', b"ello")));
///
/// assert!(utf8_char.parse(b"\xf0\x9f\x98\x8a") == Ok(Success('😊', b"")));
///
/// assert!(utf8_char.parse(b"\xff\xff\xff\xff").is_err());
/// ```
pub fn utf8_char<I: ByteInput>(input: I) -> PResult<char, I> {
    let Success(b0, rem) = input.clone().parse_byte()?;

    if (b0 & 0b1000_0000) == 0 {
        let ch = unsafe { char::from_u32_unchecked(b0.into()) };
        Ok(Success(ch, rem))
    } else if (b0 & 0b1110_0000) == 0b1100_0000 {
        let tmp = rem.clone();
        let (b1, rem) = match rem.parse_byte() {
            Ok(Success(b1, rem)) => (b1, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b1 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let b0 = u32::from(b0) & 0b0001_1111;
        let b1 = u32::from(b1) & 0b0011_1111;
        let ch = (b0 << 6) | b1;
        if let Some(ch) = char::from_u32(ch) {
            Ok(Success(ch, rem))
        } else {
            Err(Failure(Error::invalid_input(input.clone()), input))
        }
    } else if (b0 & 0b1111_0000) == 0b1110_0000 {
        let tmp = rem.clone();
        let (b1, rem) = match rem.parse_byte() {
            Ok(Success(b1, rem)) => (b1, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b1 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let tmp = rem.clone();
        let (b2, rem) = match rem.parse_byte() {
            Ok(Success(b2, rem)) => (b2, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b2 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let b0 = u32::from(b0) & 0b0000_1111;
        let b1 = u32::from(b1) & 0b0011_1111;
        let b2 = u32::from(b2) & 0b0011_1111;
        let ch = (b0 << 12) | (b1 << 6) | b2;
        if let Some(ch) = char::from_u32(ch) {
            Ok(Success(ch, rem))
        } else {
            Err(Failure(Error::invalid_input(input.clone()), input))
        }
    } else if (b0 & 0b1111_1000) == 0b1111_0000 {
        let tmp = rem.clone();
        let (b1, rem) = match rem.parse_byte() {
            Ok(Success(b1, rem)) => (b1, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b1 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let tmp = rem.clone();
        let (b2, rem) = match rem.parse_byte() {
            Ok(Success(b2, rem)) => (b2, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b2 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let tmp = rem.clone();
        let (b3, rem) = match rem.parse_byte() {
            Ok(Success(b3, rem)) => (b3, rem),
            Err(Failure(e, _)) => {
                return Err(Failure(e, input.clone()));
            }
        };
        if (b3 & 0b1100_0000) != 0b1000_0000 {
            return Err(Failure(Error::invalid_input(tmp), input));
        }
        let b0 = u32::from(b0) & 0b0000_0111;
        let b1 = u32::from(b1) & 0b0011_1111;
        let b2 = u32::from(b2) & 0b0011_1111;
        let b3 = u32::from(b3) & 0b0011_1111;
        let ch = (b0 << 18) | (b1 << 12) | (b2 << 6) | b3;
        if let Some(ch) = char::from_u32(ch) {
            Ok(Success(ch, rem))
        } else {
            Err(Failure(Error::invalid_input(input.clone()), input))
        }
    } else {
        Err(Failure(Error::invalid_input(input.clone()), input))
    }
}

/// Parser that takes one byte from the input and interprets it as a [`u8`](prim@u8).
///
/// This parser always succeeds unless the input is empty.
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::bytes::u8;
/// assert!(u8.parse(b"\x01\x02\x03\x04") == Ok(Success(1u8, b"\x02\x03\x04")));
///
/// assert!(u8.parse(b"").is_err());
/// ```
pub fn u8<I: ByteInput>(input: I) -> PResult<core::primitive::u8, I> {
    I::parse_byte(input)
}

/// Parser that takes one byte from the input and interprets it as an [`i8`](prim@i8).
///
/// This parser always succeeds unless the input is empty.
///
/// # Example
/// ```
/// # use pars::prelude::*;
/// # use pars::bytes::i8;
/// assert!(i8.parse(b"\x01\x02\x03\x04") == Ok(Success(1i8, b"\x02\x03\x04")));
///
/// assert!(i8.parse(b"\xff\xff\xff\xff") == Ok(Success(-1i8, b"\xff\xff\xff")));
///
/// assert!(i8.parse(b"").is_err());
/// ```
pub fn i8<I: ByteInput>(input: I) -> PResult<core::primitive::i8, I> {
    u8.map(u8::cast_signed).parse(input)
}

